package com.verizonwireless.brandexperience.model;

public class ProductSpotlight {

    private String titleBar;

    private String descriptionBar;

    private String buttonBar;

    public void setTitleBar(String titleBar) {
        this.titleBar = titleBar;
    }

    public String getTitleBar() {
        return titleBar;
    }

    public void setDescriptionBar(String descriptionBar) {
        this.descriptionBar = descriptionBar;
    }

    public String getDescriptionBar() {
        return descriptionBar;
    }

    public String getButtonBar() {
        return buttonBar;
    }

    public void setButtonBar(String buttonBar) {
        this.buttonBar = buttonBar;
    }
}